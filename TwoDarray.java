
public class TwoDarray {
    public static void main(String[] args) {
        int[][] tdarr={{10,20,30}, {40,50,60},{70,80,90}};
         for (int i=0; i<tdarr.length; i++){
            for (int j=0; j<tdarr[i].length; j++){
                System.out.print(tdarr[i][j] + " ");
            }
            System.out.println();

        
        }
    }


}

